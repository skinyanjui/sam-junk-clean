
import * as React from "react"
import { cn } from "@/lib/utils"

const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement> & {
    hasImage?: boolean; // Define the prop properly but don't pass to DOM
  }
>(({ className, hasImage, ...props }, ref) => (
  <div
    ref={ref}
    className={cn(
      "rounded-lg border bg-card text-card-foreground shadow-sm",
      hasImage && "overflow-hidden", // Use the prop for styling
      className
    )}
    {...props} // Don't spread hasImage to DOM
  />
))
Card.displayName = "Card"

